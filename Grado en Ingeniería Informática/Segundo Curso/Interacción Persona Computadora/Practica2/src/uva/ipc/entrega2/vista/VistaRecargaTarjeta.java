    /**
    * Restablece el estado de los botones de recarga.
    */
    
    private void iniciarbotones(){
        diezButton.setBackground(Color.LIGHT_GRAY);
        veinteButton.setBackground(Color.LIGHT_GRAY);
        cincuentaButton.setBackground(Color.LIGHT_GRAY);
    }
    
    /**
    * Obtiene el valor de recarga seleccionado por el usuario.
    *
    * @return El valor de recarga seleccionado (10, 20 o 50).
    */
    
    public int getRecarga(){
        if(diezButton.getBackground().equals(Color.white)){
            return 10;
        }
        if(veinteButton.getBackground().equals(Color.white)){
            return 20;
        }
        if(cincuentaButton.getBackground().equals(Color.white)){
            return 50;
        }
        else{
            return 0;
        }
    }
    
